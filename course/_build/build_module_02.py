# --- Section 3 : Martingale representation ---

CELLS.append(md(r"""## 3. The martingale representation theorem

Where does $Z_t$ come from? It is forced on us by the following fundamental fact.
"""))

CELLS.append(md(callout(
    "theorem",
    "Martingale Representation (for BM filtration)",
    r"""Let $(\mathcal{F}_t)$ be the filtration generated by a $d$-dimensional Brownian motion $W$. Then every square-integrable $\mathcal{F}_T$-measurable random variable $\xi$ admits a unique representation

$$\xi = \mathbb{E}[\xi] + \int_0^T Z_s\,dW_s$$

for some adapted, square-integrable process $Z$. In particular, the martingale $M_t = \mathbb{E}[\xi \mid \mathcal{F}_t]$ is a stochastic integral:

$$M_t = \mathbb{E}[\xi] + \int_0^t Z_s\,dW_s.$$"""
)))

CELLS.append(md(r"""**Consequence for BSDEs.** If $f \equiv 0$, then $Y_t = \mathbb{E}[g(X_T) \mid \mathcal{F}_t]$ is a martingale; by the theorem it equals $\mathbb{E}[g(X_T)] + \int_0^t Z_s\,dW_s$, which gives $-dY_t = -Z_t\,dW_t$. The non-trivial generator $f$ just adds a drift to the dynamics of $Y$.

**Uniqueness of $Z$.** The representation is unique up to $dt \otimes d\mathbb{P}$-null sets. This is what makes BSDEs *well-posed*: for each admissible $Y$, there is exactly one $Z$ that makes the equation work.
"""))

